void disp_char(char c, u8 xx, u8 yy, u8 cc){
	//u8 *VGA = (u8*)vid_mem;

	const u8 *char_to_disp = font8x8_basic[(u8) c];
	for(u8 y = 0; y < 8; y++){
		for (u8 x = 0; x < 8; x++){
			if(char_to_disp[y] & (1 << x)){//if pixel is 1 or 0
				int offset = (8*xx+x) + 320 * (8*yy+y);
				//VGA[offset] = cc;
                back_buff[offset] = cc;
			}
		}
	}
}

void disp_char_absolute(char c, u16 xx, u16 yy, u8 color){
	//u8 *VGA = (u8*)vid_mem;

	const u8 *char_to_disp = font8x8_basic[(u8) c];
	for(u8 y = 0; y < 8; y++){
		for (u8 x = 0; x < 8; x++){
			if(char_to_disp[y] & (1 << x)){//if pixel is 1 or 0
				int offset = (xx+x) + 320 * (yy+y);
				//VGA[offset] = color;
                back_buff[offset] = color;
			}
		}
	}
}

void disp_string_absolute(char *ch, u16 x, u16 y, u8 cc){
    char *str = ch;
    char c = 0;
    int xx = 0;
    while ((c = *str++) != 0){
        disp_char_absolute(c, 8*xx+x, y, cc);
        xx++;
    }
}
void disp_string(char *ch, u8 x, u8 y, u8 cc){
    char *str = ch;
    char c = 0;
    int xx = 0;
    while ((c = *str++) != 0){
        disp_char(c, (xx+x), y, cc);
        xx++;
    }
}

void disp_int(u32 n, u8 x, u8 y, u8 cc){

    if (n == 0)
        disp_char_absolute('0', x, y, cc);

    u32 num = n;
    u32 tmp = n;

    u8 num_digits = 0;

    while (tmp >= 1){
        tmp *= 0.1f;
        num_digits++;
    }

    u8 digit;
    u8 xx = num_digits;
    while (num >= 1) {
        digit = num % 10;
        num *= 0.1f;
        disp_char_absolute(digit+'0', x+xx*8, y, cc);
        xx--;
    }
}

