void disp_circle(u8 xx, u8 yy, u8 size, u8 background, u8 forground){

    u32 offset = 0;

    for(char row = -size; row < size; row++){
        for(char col = -size; col < size; col++){
            offset = (xx+col+size) + 320*(yy+row+size);

            if((col)*(col) + (row)*(row) <= size * size
            ){

                back_buff[offset] = forground;

            }
            else
            {
                back_buff[offset] = background;
            }
        }
    }

}
